# ╔═╡ 13e14c2b-e821-44ce-a543-1a40a2e159c6
info_t(t, a, r) = (t^2 * a * exp(-r*t)) / (1 + a*exp(-r*t))^2

# ╔═╡ 715449aa-e8e2-4f9d-ad25-39abf191480c
function nopt(tmax, q₀, r; ε=0.15r, α=0.05)
	Z = quantile(Normal(), α/2)
	a = 1/q₀ - 1
	ℐ = sum(info_t.(0:tmax, a, r))
	return (Z/ε)^2 / ℐ
end

# ╔═╡ 89d9e46a-39de-4193-a0a1-749473221ab5
function find_tmax(qmax, q₀, r)
	a = 1/q₀ - 1
    -ceil(log((1-qmax)/(a*qmax)) / r)
end

